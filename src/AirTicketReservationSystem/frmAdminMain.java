    private void lblLogoutMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_lblLogoutMouseClicked

            int userChoice = JOptionPane.showConfirmDialog(null,"Do you want to logout?", "Logout Confirm", JOptionPane.YES_NO_OPTION);
            if(userChoice==0){
                MDIForm.loggedin=false;
                MDIForm.loggedUserID="";
                MDIForm.loggedUserAccess="";
                MDIForm.DesktopPane.removeAll();
                MDIForm.DesktopPane.repaint();
                frmSignin s =new frmSignin();
                MDIForm.DesktopPane.add(s);
                int height = MDIForm.DesktopPane.getHeight();
                int width = MDIForm.DesktopPane.getWidth();
                int x = (int)(height-s.getHeight())/2;
                int y = (int)(width-s.getWidth())/2;
                s.setLocation(y,x);
                s.setVisible(true);            
            }
    }//GEN-LAST:event_lblLogoutMouseClicked

